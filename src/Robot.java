public class Robot {
    public Head head;
    public Body body;
    public Hand hand1;
    public Hand hand2;
    public Leg leg;

    public Robot(Head head, Body body, Hand hand1, Hand hand2, Leg leg) {
        this.head = head;
        this.body = body;
        this.hand1 = hand1;
        this.hand2 = hand2;
        this.leg = leg;
    }
}
